function showNavigation() {
    let list = document.querySelector(".container-aside-list");
    list.classList.toggle("container-menu-show");
    let cross = document.querySelector(".container-cross");
    cross.classList.toggle("list_close")
}

function closeNavigation() {
    let list = document.querySelector(".container-aside-list");
    list.classList.toggle("container-menu-show");
    let cross = document.querySelector(".container-cross");
    cross.classList.toggle("list_close")
}